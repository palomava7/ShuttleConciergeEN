Personales en Posesión de Particulares, Shuttle Concierge pone a tu 

disposición el siguiente Aviso de Privacidad:</p>

<h3>01.- Responsable de la Protección de sus Datos Personales.</h3>

<p>Shuttle Concierge, con dirección fiscal ubicada en Circuito de Hornos 1589-B,

Supermanzana 200, Manzana 80, Lote 2, Cancún, Quintana Roo, C.P. 77539</p>

<h3>02.- Formas para Obtener los Datos Personales y Datos Recabados.</h3>

<p>Los datos personales que Shuttle Concierge recabe de usted, serán destinados 

únicamente para los fines descritos en el presente aviso de privacidad y podrán 

ser recabados:</p>
<p>• De manera directa, ya sea a través de nuestros representantes de

reservaciones, de manera personal o a través de nuestro centro de 

reservación telefónica.</p>

<p>• Ingresando sus datos o utilizando los servicios en línea a través de

la página www.shuttleconcierge.com,  o cualquiera de nuestros sitios.</p>

<p>Los datos que Shuttle Concierge solicite a usted, serán los siguientes:</p>

<p> • Nombre completo con apellidos </p>
<p> • Número de teléfono </p>
<p> • Correo electrónico </p>
<p> • Domicilio </p>
<p> • Datos financieros como lo son: Número de tarjeta de crédito, nombre del

titular de la misma, fecha de vencimiento, código de seguridad y cualquier 

otro dato que resulte necesario para concluir las reservaciones solicitadas 

por usted. </p>

<h3>03.- Datos Personales Sensibles.</h3>

<p>Para cumplir con las finalidades detalladas en el Presente Aviso de Privacidad, 

también le podrán ser recabados y tratados datos personales sensibles, como 

parentescos, condición física, estado de salud, discapacidad, documentos 

médicos, entre algunos otros. Sus datos personales sensibles serán tratados con 

las más estrictas medidas de seguridad para garantizar su confidencialidad.</p>

<h3>04.- Finalidad de los Datos Personales recabados.</h3>

<p>Sus datos personales serán utilizados con fines de procesamiento, seguimiento,

actualización, modificación, cancelación y confirmación de los servicios por usted 

reservados con Shuttle Concierge a través de sus medios, con fines financieros y 

crediticios, para dar cumplimiento a las obligaciones contraídas con y por usted y 

con nuestros proveedores, evaluar la calidad del servicio, realizar sondeos sobre 

hábitos y preferencias de viaje, para participar en concursos y sorteos, así como 

para el envío de  promociones, ofertas, servicios y  otros fines publicitarios y 

promocionales.</p>

<h3>05.- Transferencia de datos, nacional e internacional.</h3>

<p>Sus datos personales podrán ser transferidos y/o compartidos a: socios

comerciales, afiliados,  proveedores, Instituciones financieras, bancarias y 

crediticias, sociedades subsidiarias, filiales, afiliadas y terceros; como lo son 

Hoteles, Arrendadoras, Aerolíneas, Proveedores de Tours, etc, para confirmar sus 

reservaciones y cumplimiento de la finalidad de los datos recabados establecida 

en el punto 4 del presente Aviso de Privacidad.</p>

<h3>06.- Uso de cookies y web beacons.</h3>

<p>Hacemos de su conocimiento que contamos con tecnologías electrónicas (cookies

y web beacons), que nos permiten recabar datos de su actividad de manera 

electrónica y simultánea al tiempo que usted hace contacto con nosotros a través 

de nuestra página de internet: www.shuttleconcierge.com, los datos que 

recabamos son los siguientes:</p>
<p>1. Información del tipo de explorador que utiliza.</p>
<p>2. Información del horario y tiempo que permaneció en nuestra página.</p>
<p>3. Secciones consultadas en nuestra página.</p>
<p>4. Información de las Páginas que visitó previo de ingresar a:

www.shuttleconcierge.com</p>
<p>5. Información de las Páginas que visitó posterior a haber ingresado a:

www.shuttleconcierge.com</p>
<p>Los datos recabados, no son necesarios para cumplir con las obligaciones 

primarias señaladas en el presente Aviso de Privacidad, sin embargo, nos 

son de suma importancia y utilidad para cumplir con las siguientes 

finalidades:</p>
<p>1. Enviar información a su correo electrónico relacionado a nuestras rutas.</p>
<p>2. Enviar publicidad relacionada a sus gustos y preferencias.</p>
<p>3. Hacerle llegar información relacionada a nuestros descuentos, paquetes y

promociones.</p>
<p>4. Ofrecerle nuevos productos, destinos y servicios basados en sus preferencias.</p>
<p>Los datos recabados serán uso exclusivo de Shuttle Concierge, por lo tanto dichos 

datos no serán sujetos de transferencias nacionales o internacionales.</p>
<p>De igual manera, hacemos de su conocimiento que en todo momento usted puede 

deshabilitar el uso de las cookies.</p>
<h3>07.- Ejercicio de los Derechos de Acceso, Rectificación, Cancelación,

Oposición (“Derechos ARCO”).</h3>
<p>La Ley le otorga cuatro derechos a ejercer:</p>
<p>Acceso a sus datos personales para conocer cuáles se encuentran

resguardados, si son correctos o actualizados y para qué finalidades se 

utilizan.</p>
<p>Rectificación en caso de ser inexactos, incorrectos o estén

desactualizados.</p>
<p>Cancelación consistente en la eliminación de sus datos personales

cuando considere que no están siendo utilizados para el cumplimiento de 

los fines establecidos en el presente Aviso de Privacidad.</p>
<p>Oposición al tratamiento de sus datos personales respecto a

alguno de los fines establecidos en el presente Aviso de Privacidad.</p>
<h3>08.- Derecho para cancelar su consentimiento acerca del tratamiento de

datos.</h3>
<p>Le informamos que usted puede manifestar su derecho de revocar el

consentimiento otorgado para el tratamiento de sus datos personales, con el 

propósito que se deje de hacer uso de los mismos, asimismo, usted puede revocar 

su consentimiento a recibir promociones y ofertas de manera telefónica o por 

correo electrónico, siguiendo el procedimiento establecido en el punto 8 siguiente.</p>
<p>No obstante, Shuttle Concierge y sus filiales le informan que el mantener sus

datos personales en su base de datos, le permitirá entender mejor sus 

necesidades con base a su historial y con ello ofrecerle un mejor servicio en sus 

futuras reservaciones.</p>
